GC_LEFT:
    PUSH BC: PUSH AF: PUSH DE
    LD A, (HL)
    PUSH AF
    LD A, NEGRO
    PUSH HL
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    POP HL
    LD BC, $FFFD
    ADD HL, BC
    POP AF
    PUSH HL
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    POP HL
    POP DE: POP AF: POP BC

    RET

GC_RIGHT:
    PUSH DE: PUSH BC: PUSH AF
    LD A, (HL)
    PUSH AF
    LD A, NEGRO
    PUSH HL
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    POP HL
    LD BC, $3
    ADD HL, BC
    POP AF
    PUSH HL
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    POP HL
    POP AF: POP BC: POP DE

    RET
