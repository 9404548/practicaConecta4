GC_COLOR_JUGADOR_ACTUAL:
    LD A, D
    ADD BLINK
    PUSH AF
    LD HL, $5845
    PUSH HL
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    CALL INC_HL_3X3
    POP HL
    POP AF
    RET
